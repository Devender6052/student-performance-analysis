# Range fumction returns the sequence of number,starting from 0 by default,increments by 1 by default, and stops before a specified number
# Range(start?,stop,step?)

for el in range(5):
    print(el)
print("first loop completed")
for el in range(1,5):
    print(el)
print("second loop completed")
for el in range(1,5,2):
    print(el)
print("third loop completed")